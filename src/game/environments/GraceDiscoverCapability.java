package game.environments;

/**
 * Enum that can be used in capability set to tell us whether a
 *  Site of Lost Grace has been discovered or not.
 *
 * Created by:
 * @author Arosh Heenkenda
 *
 * Modified by:
 *
 */
public enum GraceDiscoverCapability {

    /**
     * Has been discovered
     */
    DISCOVER_TRUE,

    /**
     * Has not been discovered
     */
    DISCOVER_FALSE
}
